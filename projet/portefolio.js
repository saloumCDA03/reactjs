/**
        portfolio
Créez un site qui reprend votre CV, des projets et du contenus que vous avez créez (si vous en avez), avec des images, des paragraphes et plusieurs pages.

Objectif : 
    Créer un site web personnel qui fonctionne comme un portfolio pour mettre en valeur vos compétences, votre CV, vos projets et d'autres contenus que vous avez créés, en utilisant uniquement React comme bibliothèque.

Technologies à Utiliser :
Reactjs

Fonctionnalités Requises :
Page d'Accueil : 
    Une page d'accueil accueillante qui présente brièvement qui vous êtes et ce que les visiteurs peuvent trouver sur votre site.

CV : 
    Une page dédiée à votre CV qui inclut des sections telles que l'éducation, l'expérience professionnelle, les compétences, et tout autre élément pertinent.

Projets : 
    Une page où vous présentez les projets que vous avez réalisés. 
    Chaque projet doit avoir une description, des images, des liens vers le code source (GitHub, GitLab, etc.) et, si possible, des démonstrations en direct.


Navigation : 
    Utilisez la gestion de la navigation en React ( en utilisant React Router) pour permettre une navigation fluide entre les différentes pages de votre site.

Mise en Page Responsive : 
    Assurez-vous que votre site est responsive, de sorte qu'il s'adapte correctement aux différents appareils .

Design Visuellement Attrayant : 
    Concevez votre site de manière à ce qu'il soit visuellement attrayant et professionnel en utilisant uniquement CSS pour le style.

Formulaire de Contact : 
    Incluez un formulaire de contact .

Réseaux Sociaux : 
    Intégrez des liens vers vos profils de réseaux sociaux professionnels (LinkedIn, GitHub, etc.) si vous le souhaitez.

Optionnel (pour aller plus loin) :

Système d'Authentification : Mettez en place un système d'authentification pour gérer l'accès à certaines parties du site (par exemple, des articles de blog réservés aux utilisateurs enregistrés).

Commentaires : Permettez aux visiteurs de laisser des commentaires sur vos projets ou articles de blog en utilisant React pour gérer ces interactions.

*/
